export function SlideIn({
  children,
  left = false,
  right = false,
  delay = 0,
  className = "",
}: {
  children: React.ReactNode;
  left?: boolean;
  right?: boolean;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, x: left ? -40 : right ? 40 : 0 }}
      whileInView={{ opacity: 1, x: 0 }}
      viewport={{ once: true, margin: "-10%" }}
      transition={{ duration: 0.8, delay, ease: [0.21, 0.47, 0.32, 0.98] }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
